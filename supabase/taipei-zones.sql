GRANT SELECT ON public.taipei_zone_summary TO service_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON TABLE public.taipei_zone_settings TO service_role;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO service_role;

NOTIFY pgrst, 'reload schema';

